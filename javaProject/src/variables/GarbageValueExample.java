package variables;

public class GarbageValueExample {

	public static void main(String[] args) {
		byte var1 = 125;
		int var2 = 125;
		
		
		
		//>(                 )<까지 ()번 반복하겠다
		for(int i=0; i<5; i++) {
		//i의 값에다가 한번실행할떄(반복할때)마다	i의값에 1을 증가시켜라
			var1++;
			var2++;
			System.out.println("var1: " + var1 + "\t"  + "var2: " );
			
			// \
		}

	}

}
