public class Printer {
		
	public static void println(int x1) {
		System.out.println(x1);
	}
	public static void println(boolean x1) {
		System.out.println(x1);
	}
	public static void println(double x1) {
		System.out.println(x1);
	}
	public static void println(String x1) {
		System.out.println(x1);
	}
	
}
